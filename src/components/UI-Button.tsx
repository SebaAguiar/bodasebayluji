/****************************************************************************************************************************************************
 * * IMPORTS
 ****************************************************************************************************************************************************/

import React from 'react';

/****************************************************************************************************************************************************
 * * TYPES - INTERFACES - CLASES
 ****************************************************************************************************************************************************/

type TypeMyButtonProps = {
  buttonText: string;
  tailwindStyle?: string;
  cssStyle?: object;
  type?: 'submit' | 'button';
  handleClick: () => void;
};

/****************************************************************************************************************************************************
 * * DECLARATIONS
 ****************************************************************************************************************************************************/

/****************************************************************************************************************************************************
 * * FUNCTIONS
 ****************************************************************************************************************************************************/

const UIButton: React.FC<TypeMyButtonProps> = ({
  buttonText,
  type,
  tailwindStyle,
  cssStyle,
  handleClick,
}) => {
  return (
    <>
      <button
        className={
          tailwindStyle ??
          'h-10 w-32 rounded-md montserratFont border-2 font-semibold mt-4 ml-6 transition-transform ease-in-out duration-300 hover:bg-grey'
        }
        type={type ?? 'button'}
        onClick={handleClick}
      >
        {buttonText}
      </button>
    </>
  );
};

/****************************************************************************************************************************************************
 * * EXPORTS
 ****************************************************************************************************************************************************/

export default UIButton;

/****************************************************************************************************************************************************
 * * IMPORTS
 ****************************************************************************************************************************************************/

import React from 'react';
import { AnimatePresence, motion } from 'framer-motion';

/****************************************************************************************************************************************************
 * * TYPES - INTERFACES - CLASES
 ****************************************************************************************************************************************************/

type TypePagesProps = {
  children: React.ReactNode;
  style: string;
};

/****************************************************************************************************************************************************
 * * DECLARATIONS
 ****************************************************************************************************************************************************/

/************************s****************************************************************************************************************************
 * * FUNCTIONS
 ****************************************************************************************************************************************************/

const PagesMain: React.FC<TypePagesProps> = ({ children, style }) => {
  return (
    <>
      <AnimatePresence>
        <motion.main
          initial={{ opacity: 0, y: 50 }}
          animate={{ opacity: 1, y: 0 }}
          exit={{ opacity: 0, y: 50 }}
          transition={{
            duration: 3,
            delay: 1,
            ease: [0, 0.71, 0.2, 1.01],
          }}
          className={style}
        >
          {children}
        </motion.main>
      </AnimatePresence>
    </>
  );
};

/****************************************************************************************************************************************************
 * * EXPORTS
 ****************************************************************************************************************************************************/

export default PagesMain;
